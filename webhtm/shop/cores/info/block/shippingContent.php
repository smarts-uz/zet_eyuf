<h3>Процесс начала формирования заказа:</h3>
<h4>При онлайн оплате.</h4>
<p>Процесс доставка при онлайн оплате начинается сразу после оплаты заказа. После оплаты наши сборщики продуктов
    начинают формировать корзину на основе вашего заказа. После формирования наша курьерская служба доставляет ваш заказ
    по указанному вами адресу.</p>
<h4>При пост оплате.</h4>
<p>Процесс доставки при пост оплате начинается сразу после подтверждения Покупателем заказа оператору при телефонном
    звонке. Сразу после оформления заказа наш оператор звонит Вам по указанному номеру телефона для подтверждения
    заказа. После подтверждения заказа сборщики продуктов начинают формировать корзину на основе заказа. После
    формирования корзины наша курьерская служба доставляет Вам заказ по указанному вами адресу. Получив заказ Вам
    необходимо оплатить заказ наличными или по терминалу курьеру.</p>
<h4>Важно</h4>
<p> Сразу после оплаты на сайте создается ваш личный кабинет где вы можете прослеживать статус вашего заказа. На
    основе каждого заказа формируется товарная накладная где указываются все заказанные продукты, их количество и цена.
    Данную накладную можно распечатать и сохранить в PDF формате. Необходимо сверить продуктовую номенклатуру, которая
    указана в накладной с доставкой.</p>
